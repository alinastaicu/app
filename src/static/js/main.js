function numberWord(nr) {
  if (nr == 1) {
    return 'unu';
  } else if (nr == 2) {
    return 'doi';
  } else if (nr == 3) {
    return 'trei';
  } else if (nr == 4) {
    return 'patru';
  } else if (nr == 5) {
    return 'cinci';
  } else if (nr == 6) {
    return 'sase';
  } else if (nr == 7) {
    return 'sapte';
  } else if (nr == 8) {
    return 'opt';
  } else if (nr == 9) {
    return 'noua';
  }
}
